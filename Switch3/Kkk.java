package Switch3;

public class Kkk {
    public void Notas(int t){
        switch(t){
            case 1:
                System.out.println("5.0");
                break;

                case 2:
                    System.out.println("3.0");
                    break;

                    case 3:
                        System.out.println("1.0");

            default:
                System.out.println("nota invalida, digita una nota que sea valida");
        }
    }
}
